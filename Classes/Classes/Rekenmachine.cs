using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Classes
{
    internal class Rekenmachine
    {
        internal int add(int num1, int num2)
        {
            return num1 + num2;
        }

        internal int divide(int num1, int num2)
        {
            return num1 / num2;
        }

        internal int multiply(int num1, int num2)
        {
            return num1 * num2;
        }
    }
}
